function slugify($text)
{
    // Replace accented characters
    $text = iconv('UTF-8', 'ASCII//TRANSLIT', $text);
    $text = strtolower($text);
    $text = preg_replace('/[^a-z0-9]+/', '-', $text); // replace non-alphanum with dash
    return trim($text, '-');
}


